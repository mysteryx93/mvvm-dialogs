namespace MvvmDialogs.Core.FrameworkDialogs
{
    /// <summary>
    /// Defines the known folders for custom places in file dialog boxes.
    /// </summary>
    public enum FileDialogCustomPlaces
    {
        /// <summary>
        /// The Contacts folder for the current user.
        /// </summary>
        Contacts,
        /// <summary>
        /// The Internet cookies folder for the current user.
        /// </summary>
        Cookies,
        /// <summary>
        /// The folder for storing files on the desktop for the current user.
        /// </summary>
        Desktop,
        /// <summary>
        /// The Documents folder for the current user.
        /// </summary>
        Documents,
        /// <summary>
        /// The Favorites folder for the current user.
        /// </summary>
        Favorites,
        /// <summary>
        /// The folder for application-specific data that is used by the current, non-roaming user.
        /// </summary>
        LocalApplicationData,
        /// <summary>
        /// The Music folder for the current user.
        /// </summary>
        Music,
        /// <summary>
        /// The Pictures folder for the current user.
        /// </summary>
        Pictures,
        /// <summary>
        /// The Program Files folder.
        /// </summary>
        ProgramFiles,
        /// <summary>
        /// The folder for components that are shared across applications.
        /// </summary>
        ProgramFilesCommon,
        /// <summary>
        /// The folder that contains the program groups for the current user.
        /// </summary>
        Programs,
        /// <summary>
        /// The folder for application-specific data for the current roaming user.
        /// </summary>
        RoamingApplicationData,
        /// <summary>
        /// The folder that contains the Send To menu items for the current user.
        /// </summary>
        SendTo,
        /// <summary>
        /// The folder that contains the Start menu items for the current user.
        /// </summary>
        StartMenu,
        /// <summary>
        /// The folder that corresponds to the Startup program group for the current user.
        /// </summary>
        Startup,
        /// <summary>
        /// The System folder.
        /// </summary>
        System,
        /// <summary>
        /// The folder for document templates for the current user.
        /// </summary>
        Templates
    }
}
